async function main() {
  console.log("🌱 Starting seed...")

  for (const topic of topics) {
    const { questions, ...topicData } = topic

    const upsertedTopic = await prisma.topic.upsert({
      where: { slug: topicData.slug },
      update: topicData,
      create: topicData,
    })

    // Delete existing questions to re-seed cleanly
    await prisma.question.deleteMany({ where: { topicId: upsertedTopic.id } })

    await prisma.question.createMany({
      data: questions.map((q) => ({
        ...q,
        topicId: upsertedTopic.id,
      })),
    })

    console.log(`  ✓ ${topicData.title} (${questions.length} questions)`)
  }

  const topicCount = await prisma.topic.count()
  const questionCount = await prisma.question.count()
  console.log(`\n✅ Seed complete: ${topicCount} topics, ${questionCount} questions`)
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
